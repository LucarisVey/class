public class Aircraft {
    private String model;
    private int speedMax;
    private int yearOfFirstFlight;

    public String getModel() {

        return model;
    }
    public void setModel(String model) {

        this.model = model;
    }
    public int getSpeedMax() {
        return speedMax;
    }
    public void setSpeedMax(int speedMax) {

        this.speedMax = speedMax;
    }
    public int getYearOfFirstFlight() {

        return yearOfFirstFlight;
    }
    public void setYearOfFirstFlight(int yearOfFirstFlight) {

        this.yearOfFirstFlight = yearOfFirstFlight;
    }
}
